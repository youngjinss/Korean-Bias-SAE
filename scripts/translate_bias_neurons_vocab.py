def load_english_vocab(vocab_type='negative'):
    """Load English vocabulary from Bias-Neurons"""
    base_path = Path(__file__).parent.parent.parent / "Bias-Neurons" / "bias_neuron_data"
    filename = f"{'neg' if vocab_type == 'negative' else 'pos'}_adj_vocab.json"

    with open(base_path / filename, 'r') as f:
        return json.load(f)

def translate_and_select(english_adjectives, translation_dict, target_count):
    """Translate English adjectives to Korean and select top N"""
    translated = []

    for adj in english_adjectives:
        adj_lower = adj.lower()
        if adj_lower in translation_dict:
            translated.append(translation_dict[adj_lower])

    # Remove duplicates while preserving order
    seen = set()
    unique_translated = []
    for adj in translated:
        if adj not in seen:
            seen.add(adj)
            unique_translated.append(adj)

    # Return up to target_count
    return unique_translated[:target_count]

def main():
    # Load English vocabularies
    print("Loading Bias-Neurons English vocabularies...")
    neg_english = load_english_vocab('negative')
    pos_english = load_english_vocab('positive')

    print(f"  Loaded {len(neg_english)} negative adjectives")
    print(f"  Loaded {len(pos_english)} positive adjectives")

    # Generate for different stages
    stages = {
        'medium': 50,
        'full': 300
    }

    output_dir = Path(__file__).parent.parent / "data" / "modifiers"

    for stage, count in stages.items():
        print(f"\nGenerating {stage} stage ({count} adjectives each)...")

        # Translate and select
        neg_korean = translate_and_select(neg_english, NEGATIVE_TRANSLATIONS, count)
        pos_korean = translate_and_select(pos_english, POSITIVE_TRANSLATIONS, count)

        print(f"  Translated {len(neg_korean)} negative adjectives")
        print(f"  Translated {len(pos_korean)} positive adjectives")

        # Save to JSON files
        neg_file = output_dir / f"{stage}_negative_ko.json"
        pos_file = output_dir / f"{stage}_positive_ko.json"

        with open(neg_file, 'w', encoding='utf-8') as f:
            json.dump(neg_korean, f, ensure_ascii=False, indent=2)

        with open(pos_file, 'w', encoding='utf-8') as f:
            json.dump(pos_korean, f, ensure_ascii=False, indent=2)

        print(f"  ✅ Saved to {neg_file} and {pos_file}")

    print("\n✅ Translation complete!")

if __name__ == "__main__":
    main()
